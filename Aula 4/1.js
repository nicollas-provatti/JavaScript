// INTRODUÇÃO

// Um array em JS é um objeto, é dinâmico e pode armazenar elementos de tipos diferentes.

// Declaração
const numeros = []

// Atribuição
numeros[0] = 1
numeros[1] = 2
numeros[2] = 3

console.log(numeros)

// Inicialização
const notas = [7.6, 4.5, 8.7]
console.log(notas)

// Dinâmico
const alunos = ['Bia', 'Carlos', 'Ana']
console.log(alunos)
console.log(alunos.length)

alunos[3] = 'Jorge'
console.log(alunos)
console.log(alunos.length)

// Buracos
alunos[6] = 'Maria'
console.log(alunos)
console.log(alunos.length)

console.log(alunos[4])

// Valores variados
const valores = [1, 1.7, "olá", true]
console.log(valores)


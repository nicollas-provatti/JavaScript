// 🔹 Definição de um valor inicial
let num = 5;

console.log(num);

// 1️⃣ Pós-incremento (num++) → Primeiro usa o valor, depois incrementa
console.log(num++); 

// 2️⃣ Pré-incremento (++num) → Primeiro incrementa, depois usa o valor
console.log(++num); 

// 3️⃣ Pós-decremento (num--) → Primeiro usa o valor, depois decrementa
console.log(num--); 

// 4️⃣ Pré-decremento (--num) → Primeiro decrementa, depois usa o valor
console.log(--num); 

/* 
📌 RESUMO 📌
✔ `++num` → Incrementa **antes** de usar o valor.
✔ `num++` → Usa o valor **antes** de incrementar.
✔ `--num` → Decrementa **antes** de usar o valor.
✔ `num--` → Usa o valor **antes** de decrementar.

⚠ A diferença importa quando o operador é usado dentro de expressões!
*/


